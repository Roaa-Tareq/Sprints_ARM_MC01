#define     GPT_64_CFG_32_64_Timer      0x0
#define     GPT_16_CFG_16_32_Timer      0x4
#define     GPT_32_CFG_16_32_Timer      0x4

/***********************************************************
    GPTM Timer x Mode (GPTMTxMR) 
************************************************************/
//TAMR Field
#define     GPT_TAMR_ONE_SHOT           0x1
#define     GPT_TAMR_PERIODIC           0x2
//TACDIR Field
#define     GPT_TACDIR_COUNT_DOWN       0x10
#define     GPT_TACDIR_COUNT_UP         0x20  // The timer starts from a value of 0x0

/***********************************************************
    GPTM Control (GPTMCTL) 
************************************************************/
#define     GPT_TAEN                    0x1   // Timer A is enabled and begins counting
#define     GPT_TBEN                    0x100 // Timer B is enabled and begins counting

/***********************************************************
    GPTM Interrupt Mask (GPTMIMR) 
************************************************************/
#define     GPT_TATOIM                  0x1   // Timer A Time-Out Interrupt Mask
#define     GPT_TBTOIM                  0x100 // Timer B Time-Out Interrupt Mask

/***********************************************************
    GPTM Interrupt Clear (GPTMICR) 
************************************************************/
#define     GPT_TATOCINT                0x1   // Setting this bit clears the TATORIS bit in the GPTMRIS register and the TATOMIS bit in the GPTMMIS register.
#define     GPT_TBTOCINT                0x100 // Setting this bit clears the TBTORIS bit in the GPTMRIS register and the TBTOMIS bit in the GPTMMIS register.

