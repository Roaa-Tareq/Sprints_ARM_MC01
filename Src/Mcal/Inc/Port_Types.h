/**********************************************************************************************************************
 *  GLOBAL DATA TYPES AND STRUCTURES
 *********************************************************************************************************************/

/*
    Pin Type
/***************************************************************/
typedef Dio_ChannelType Port_PinType;

/*
    Pin Level Type
/***************************************************************/
typedef Dio_LevelType Port_PinLevelValueType;

/*
    Pin Direction Type
/***************************************************************/
typedef enum
{
    PORT_INPUT,
    PORT_OUTPUT                 
}Port_PinDirectionType;

/*
    Pin Mode Type
/***************************************************************/
typedef enum
{
    PORT_DEFAULT_M,
    PORT_DIO,
    PORT_EXINT,
    PORT_ADC,
    PORT_UART                 
}Port_PinModeType;

/*
    Pin Internal Attach Type
/***************************************************************/
typedef enum
{
    PORT_DEFAULT_IA,
    PORT_ODR,
    PORT_PUR,
    PORT_PDR             
}Port_PinInternalAttachType;

/*
    Pin Output Current Type
/***************************************************************/
typedef enum
{
    PORT_DEFAULT_OC,
    PORT_DR2R,
    PORT_DR4R,
    PORT_DR8R                 
}Port_PinOutputCurrentType;

/*
    User-defined configuration Type
/***************************************************************/
typedef struct
{
    Port_PinType                pin;
    Port_PinModeType            pinMode;
    Port_PinLevelValueType      levelValue;
    Port_PinDirectionType       pinDirection;
    Port_PinInternalAttachType  pinInternalAttach;
    Port_PinOutputCurrentType   pinOutputCurrent;
}Port_CfgType;

